package com.example.bookshare;

public class Account {

    String Username;
    String FirstName;
    String LastName;
    String Email;
    String Token;

    public Account(String username, String firstName, String lastName, String email, String token) {
        Username = username;
        FirstName = firstName;
        LastName = lastName;
        Email = email;
        Token = token;
    }
}
